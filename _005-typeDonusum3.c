#include <stdio.h>
#include <stdlib.h>

int main()
{
    float cm, metre, km ;

    printf("Lütfen cm cinsinden bir değer girin:");
    scanf("%f",&cm);

    metre=cm /100;
    km = cm /100000;

    printf("Metre cinsinden uzunluk = %.5f m \n", metre );
    printf("Kilometre cinsinden uzunluk = %.5f km" ,km );

    return 0;
}
